function calcularAreaCuadrado(lado){
    return lado*lado;
}
function calcularAreaRectangulo(base, altura){
    return base*altura;
}
console.log("Área del cuadrado de lado 2:"+ calcularAreaCuadrado(2) );
console.log("Área del rectángulo de base 2 y altura 4:"+ calcularAreaRectangulo(2, 4) );